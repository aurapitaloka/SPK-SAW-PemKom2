    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        try {
            int row = tbl_alternatif.getSelectedRow(); 
            if (row == -1) {
                JOptionPane.showMessageDialog(null, "Pilih baris yang ingin dihapus terlebih dahulu");
                return;
            }

            int jawaban = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

            if (jawaban == JOptionPane.YES_OPTION) {
                String id = tbl_alternatif.getValueAt(row, 0).toString(); 
                java.sql.Connection conn = koneksi.hubungkan();
                String sql = "DELETE FROM alternatif WHERE `id`=?";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                tampil_data();
            } else {
                JOptionPane.showMessageDialog(null, "Penghapusan data dibatalkan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal dihapus");
            e.printStackTrace();
        }

    }//GEN-LAST:event_btn_hapusActionPerformed

