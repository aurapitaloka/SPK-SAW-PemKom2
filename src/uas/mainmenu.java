   public void normalisasi() {
    try {
        // Ambil nilai maksimum dan minimum dari database
        ResultSet res = koneksi.ambildata("SELECT MAX(harga) AS maxHarga, "
                + "MIN(harga) AS minHarga, "
                + "MAX(fasilitas) AS maxFasilitas, "
                + "MAX(keamanan) AS maxKeamanan, "
                + "MAX(kebersihan) AS maxKebersihan, "
                + "MAX(rating) AS maxRating "
                + "FROM kriteria");

        float maxHarga = 0, minHarga = 0, maxFasilitas = 0, maxKeamanan = 0, maxKebersihan = 0, maxRating = 0;

        if (res.next()) {
            maxHarga = res.getFloat("maxHarga");
            minHarga = res.getFloat("minHarga");
            maxFasilitas = res.getFloat("maxFasilitas");
            maxKeamanan = res.getFloat("maxKeamanan");
            maxKebersihan = res.getFloat("maxKebersihan");
            maxRating = res.getFloat("maxRating");
        }

        // Bobot dari database
        ResultSet resBobot = koneksi.ambildata("SELECT bobot FROM kriteria_label");
        float bobotHarga = 0, bobotFasilitas = 0, bobotKeamanan = 0, bobotKebersihan = 0, bobotRating = 0;

        if (resBobot.next()) {
            bobotHarga = resBobot.getFloat("bobot");
            if (resBobot.next()) bobotFasilitas = resBobot.getFloat("bobot");
            if (resBobot.next()) bobotKeamanan = resBobot.getFloat("bobot");
            if (resBobot.next()) bobotKebersihan = resBobot.getFloat("bobot");
            if (resBobot.next()) bobotRating = resBobot.getFloat("bobot");
        }

        // Ambil semua data dari database
        ResultSet res2 = koneksi.ambildata("SELECT * FROM kriteria");
        tabelModelNormalisasi();

        while (res2.next()) {
            // Normalisasi harga dengan menggunakan nilai minimum
            float harga = minHarga / res2.getFloat("harga");
            float fasilitas = res2.getFloat("fasilitas") / maxFasilitas;
            float keamanan = res2.getFloat("keamanan") / maxKeamanan;
            float kebersihan = res2.getFloat("kebersihan") / maxKebersihan;
            float rating = res2.getFloat("rating") / maxRating;

            tabelNormalisasi.addRow(new Object[]{
                res2.getString("id"),
                res2.getString("alt"),
                harga,
                fasilitas,
                keamanan,
                kebersihan,
                rating
            });
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, ex);
    }
}


public void tentukanPeringkat() {
    try {
        // Ambil bobot dari tabel kriteria_label di database
        String sqlBobot = "SELECT * FROM kriteria_label";
        java.sql.Connection conn = koneksi.hubungkan();
        java.sql.Statement stmBobot = conn.createStatement();
        ResultSet resBobot = stmBobot.executeQuery(sqlBobot);

        // Simpan bobot dari database ke dalam map untuk setiap kriteria
        Map<String, Float> bobotMap = new HashMap<>();
        while (resBobot.next()) {
            String kriteria = resBobot.getString("kriteria");
            float bobot = resBobot.getFloat("bobot");
            bobotMap.put(kriteria, bobot);
        }

        // Inisialisasi tabel perangkingan
        tabelModelPerangkingan();

        List<Float> nilaiList = new ArrayList<>(); // List untuk menyimpan nilai

        // Iterasi setiap baris data pada tabelNormalisasi
        for (int i = 0; i < tabelNormalisasi.getRowCount(); i++) {
            // Ambil nilai dari tabelNormalisasi yang sudah dinormalisasi sebelumnya
            float harga = (float) tabelNormalisasi.getValueAt(i, 2);
            float fasilitas = (float) tabelNormalisasi.getValueAt(i, 3);
            float keamanan = (float) tabelNormalisasi.getValueAt(i, 4);
            float kebersihan = (float) tabelNormalisasi.getValueAt(i, 5);
            float rating = (float) tabelNormalisasi.getValueAt(i, 6);

            // Hitung nilai akhir dengan mengalikan nilai normalisasi dengan bobot dari kriteria_label
            float nilai = (harga * bobotMap.get("harga"))
                    + (fasilitas * bobotMap.get("fasilitas"))
                    + (keamanan * bobotMap.get("keamanan"))
                    + (kebersihan * bobotMap.get("kebersihan"))
                    + (rating * bobotMap.get("rating"));

            nilaiList.add(nilai); // Simpan nilai ke dalam list

            // Tambahkan baris ke tabelPerangkingan dengan id, alternatif, nilai, dan peringkat (masih null)
            tabelPerangkingan.addRow(new Object[]{
                tabelNormalisasi.getValueAt(i, 0), // id
                tabelNormalisasi.getValueAt(i, 1), // alt
                nilai, // nilai
                null // Tempat untuk peringkat, akan diisi kemudian
            });
        }

        // Tentukan peringkat berdasarkan nilai
        float[] nilaiArray = new float[nilaiList.size()];
        for (int i = 0; i < nilaiList.size(); i++) {
            nilaiArray[i] = nilaiList.get(i);
        }

        Arrays.sort(nilaiArray); // Urutkan nilai peringkat

        // Isi nilai peringkat ke dalam tabelPerangkingan
        for (int i = 0; i < tabelPerangkingan.getRowCount(); i++) {
            float nilai = (float) tabelPerangkingan.getValueAt(i, 2);
            // Temukan peringkat dengan mencari posisi nilai dalam nilaiArray
            int peringkat = nilaiList.size() - Arrays.binarySearch(nilaiArray, nilai);
            tabelPerangkingan.setValueAt(peringkat, i, 3); // Set peringkat ke tabel
        }

        // Urutkan tabelPerangkingan berdasarkan kolom peringkat
        DefaultTableModel model = (DefaultTableModel) tbl_peringkat.getModel();
        Vector<Vector> data = model.getDataVector();
        data.sort(Comparator.comparingInt(o -> (int) o.get(3)));

        model.fireTableDataChanged(); // Refresh tabel

        // Hapus kolom id setelah data diurutkan
        tbl_peringkat.removeColumn(tbl_peringkat.getColumnModel().getColumn(0));

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, ex);
    }
}

    
    

